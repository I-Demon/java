package Java3.Lesson07;

public class Calculator {

    public int Add(int A, int B){
        return A + B;
    }

    public int Subtract(int A, int B){
        return A - B;
    }

    public int Mult(int A, int B){
        return A * B;
    }

    public int Div(int A, int B){
        return A / B;
    }

}
